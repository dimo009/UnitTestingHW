using System;
using System.Collections.Generic;
using System.Text;

namespace Hack
{
    public class MathFunctions
    {

        public decimal GetTheAbsoluteValue(decimal value)
        {
            return Math.Abs(value);
        }

        public decimal ReturnMathFloor(decimal value)
        {
            return Math.Floor(value);
        }

    }
}
